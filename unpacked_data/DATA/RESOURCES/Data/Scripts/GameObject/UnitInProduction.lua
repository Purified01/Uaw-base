require("PGBehaviors")

local my_behavior = {
	Name = _REQUIREDNAME
}

-- --------------------------------------------------------------------------------------------------------------------
-- Behavior_Init
-- --------------------------------------------------------------------------------------------------------------------
local function Behavior_Init()
	if Object then
		UnitType = nil
		BuildLocationPtr = nil
		ObjectUnderConstructionBuildID = 0
		QueueID = 0
		TimeToCompletion = 1.0
	end
end


-- --------------------------------------------------------------------------------------------------------------------
-- Get_Unit_Type
-- --------------------------------------------------------------------------------------------------------------------
function Get_Unit_Type()
	if UnitType == nil then
		MessageBox("Build type is Nil!!!!")
		return
	end
	return UnitType
end


-- --------------------------------------------------------------------------------------------------------------------
-- Get_Build_ID
-- --------------------------------------------------------------------------------------------------------------------
function Get_Build_ID()
	return ObjectUnderConstructionBuildID 
end


-- --------------------------------------------------------------------------------------------------------------------
-- Get_Build_Location
-- --------------------------------------------------------------------------------------------------------------------
function Get_Build_Location()
	return BuildLocationPtr
end


-- --------------------------------------------------------------------------------------------------------------------
-- Get_Completion_Time
-- --------------------------------------------------------------------------------------------------------------------
function Get_Completion_Time()
	return TimeToCompletion
end


-- --------------------------------------------------------------------------------------------------------------------
-- Get_Queue_ID
-- --------------------------------------------------------------------------------------------------------------------
function Get_Queue_ID()
	return QueueID
end

-- This line must be at the bottom of the file.
