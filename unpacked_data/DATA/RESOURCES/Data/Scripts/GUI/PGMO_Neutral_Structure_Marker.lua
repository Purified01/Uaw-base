require("PGBase")
require("PGDebug")


-- ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- I N I T I A L I Z A T I O N
-- ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------

-------------------------------------------------------------------------------
--
-------------------------------------------------------------------------------
function PGMO_Neutral_Structure_Marker_Init()
	PGMO_MARKER_TYPE_NEUTRAL_STRUCTURE = "PGMO_MARKER_NEUTRAL_STRUCTURE"
end
	
-------------------------------------------------------------------------------
-- Initializes those elements which are unrelated to the 
-- LAN / Internet distinction.
-------------------------------------------------------------------------------
function On_Init()

	PGMO_Neutral_Structure_Marker_Init()
	
	-- Constants
	SIZE_STANDARD = Declare_Enum(1)
	SIZE_SMALL = Declare_Enum()
	
	-- Variables
	_ID = -1
	_Interactive = false
	_Size = SIZE_STANDARD

end


-- ---------------------------------------------------------------------------------------------------------------------
-- ---------------------------------------------------------------------------------------------------------------------
-- G U I   E V E N T   C A L L B A C K S
-- ---------------------------------------------------------------------------------------------------------------------
-- ---------------------------------------------------------------------------------------------------------------------

-------------------------------------------------------------------------------
--
-------------------------------------------------------------------------------
function On_Marker_Mouse_On() 
	this.Get_Containing_Scene().Raise_Event_Immediate("PGMO_Mouse_On_Marker", this, {PGMO_MARKER_TYPE_NEUTRAL_STRUCTURE})
end

-------------------------------------------------------------------------------
--
-------------------------------------------------------------------------------
function On_Marker_Mouse_Off() 
	this.Get_Containing_Scene().Raise_Event_Immediate("PGMO_Mouse_Off_Marker", this, {PGMO_MARKER_TYPE_NEUTRAL_STRUCTURE})
end

-------------------------------------------------------------------------------
--
-------------------------------------------------------------------------------
function Play_Click() 
	Play_SFX_Event("GUI_Generic_Button_Select")
end

-------------------------------------------------------------------------------
--
-------------------------------------------------------------------------------
function Play_Alien_Steam() 
	Play_SFX_Event("SFX_Anim_Alien_Walker_Hydraulics")
end


-- ---------------------------------------------------------------------------------------------------------------------
-- ---------------------------------------------------------------------------------------------------------------------
-- G U I   D I S P L A Y   F U N C T I O N S
-- ---------------------------------------------------------------------------------------------------------------------
-- ---------------------------------------------------------------------------------------------------------------------

-------------------------------------------------------------------------------
--
-------------------------------------------------------------------------------
function Refresh_UI() 
	if (_Size == SIZE_STANDARD) then
		this.Quad_Marker_Standard.Set_Hidden(false)
		this.Quad_Marker_Small.Set_Hidden(true)
	elseif (_Size == SIZE_SMALL) then
		this.Quad_Marker_Standard.Set_Hidden(true)
		this.Quad_Marker_Small.Set_Hidden(false)
	end
end


